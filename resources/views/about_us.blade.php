                of these public harassment issues are the main concerns of our system. We will take the reports from
                individual and analyze them from time to time. The reports will be analyzed by a group of expert
                reviewers and then published in our site. People can see their stories as well as share them on
                other social media. This way we will be able to create mass awareness about certain problems that
                will help put pressure on the respective authorities to take necessary actions.
